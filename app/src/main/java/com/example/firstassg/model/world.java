package com.example.firstassg.model;

public class world {
    private String year;
    private String str;

    public world(String year, String str) {
        this.year = year;
        this.str = str;
    }

    public String getYear() {
        return year;
    }

    public String getStr() {
        return str;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setStr(String str) {
        this.str = str;
    }
}
